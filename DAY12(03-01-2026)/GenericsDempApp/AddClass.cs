using System;
class AddClass//non generics class
{
  public int AddInt(int n1,int n2)
  {
    return n1+n2;
  }
  public double AddDouble(double n1,double n2)
  {
    return n1+n2;
  }
  public float AddFloat(float n1, float n2)
  {
    return n1+n2;
  }
  public string AddString(string s1,string s2)
  {
    return s1+s2;
  }
}